public class MultipleParameters {
    static void myMethod(String Name,int age){
        System.out.println(Name + " is " + age + " yrs old.");
    }
    public static void main(String[] args) {
        System.out.println("*** Multiple Parameters JAVA Concept ***");
        myMethod("Rakesh", 20);
        myMethod("Rahul", 24);
        myMethod("Rajiv", 29);
    }
}



