public class ArrayLoop {
    public static void main(String[] args) {
        System.out.println("*** JAVA Array Loop Concept ***");
        // Java For loop used, and the length property to specify how many times the loop should run
        String[] Bike = {"Honda","Yamaha","TVS","Kawasaki","Pulser"};
        for (int i = 0; i < Bike.length ;i++){
            System.out.println(Bike[i]);
        }
    }
}



