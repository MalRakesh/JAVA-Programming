public class BreakConcept {
    public static void main(String[] args) {
        System.out.println("*** JAVA Break Concept ***");
        // break statement is used to jump out of a loop
        for (int i = 0; i < 10; i++) {
            if (i == 6) {
                break;
            }
            System.out.println(i);
        }
    }
}



