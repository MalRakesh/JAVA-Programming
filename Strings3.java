public class Strings3 {
    public static void main(String[] args) {
        System.out.println("*** Strings - Special Characters ***");
        String txt = "Today is Day 10 of \"100 Days Coding Challenge\" "; //  \" = Double Quote
        String txt1 = "It\'s alright doing coding challenge"; //    \' = ' single Quote
        String txt2 = "\\Backslash";    //  \\ = \ Backslash
        // Escape Sequence In JAVA
        String txt3 = "Hello \nDevelopers";    //  \n = new line
        String txt4 = "Hii\tFriends";    //  \t = tab
        String txt5 = "Heyy\b There!";    // \b = Backspace
        System.out.println(txt);
        System.out.println(txt1);
        System.out.println(txt2);
        System.out.println(txt3);
        System.out.println(txt4);
        System.out.println(txt5);
    }
}







