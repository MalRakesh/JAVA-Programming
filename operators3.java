public class operators3 {
    public static void main(String[] args) {
        System.out.println("Logical Operators");
        int a = 5;
        // Logical AND
        System.out.println("a && b = "+(a>1 && a<9 ));

        // Logical OR
        System.out.println("a && b = "+(a<8 || a<9 ));
        
        // Logical NOT
        System.out.println("a && b = "+ !(a<1 && a>9 ));
    }
}


