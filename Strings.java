public class Strings {
    public static void main(String[] args) {
        System.out.println("*** Strings in JAVA ***");
        String txt = "Hello";
        String a = "100 Days Coding Challenge";
        System.out.println("The Length Of a is "+ (a.length()));  //finding length
        System.out.println(txt.toLowerCase());  //make the value in lowercase
        System.out.println(txt.toUpperCase());  //make the value in lowercase
        System.out.println(a.indexOf("Challenge")); //locate the character
    }
}





