public class Strings1 {
    public static void main(String[] args) {
        String Fname = "Rakesh";
        String Lname = "Mal";
        String greet = "Hello ";
        System.out.println("*** Concatenation in JAVA ***");
        System.out.println(Fname + " " + Lname);
        System.out.println(greet.concat(Fname)); //using concat() method
    }
}



