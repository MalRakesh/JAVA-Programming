public class Conditional3 {
    public static void main(String[] args) {
        int time = 12;

        System.out.println("*** Else If Condition ***");
        if (time < 10) {
        System.out.println("Good Morning");
        } else if (time < 18) {
        System.out.println("Good Afternoon");
        } else {
        System.out.println("Good Evening.");
        }
    }
}


