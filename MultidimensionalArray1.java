public class MultidimensionalArray1 {
    public static void main(String[] args) {
        System.out.println("*** JAVA Multi-Dimensional Array ***");
        // change the value of an element in Multi-Dimensional Array
        int[][] num = {{1,3,5,7,9},{2,4,6,8,10},{11,22,33,44,55}};
        num[1][2] = 27;
        System.out.println(num[1][2]);
    }
}



