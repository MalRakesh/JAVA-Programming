public class MultipleAttributes {
    String Fname = "Rakesh";
    String Lname = "Mal";
    int age = 21;

    public static void main(String[] args) {
        System.out.println("*** JAVA Multiple Attributes Concept ***");
        MultipleAttributes objA = new MultipleAttributes();
        System.out.println("First Name : " + objA.Fname);
        System.out.println("Last Name : " + objA.Lname);
        System.out.println("Age: " + objA.age); 
    }
}





