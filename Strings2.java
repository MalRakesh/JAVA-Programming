public class Strings2 {
    public static void main(String[] args) {
        System.out.println("*** Java Strings And Numbers ***");
        int x = 24;
        int y = 9;
        int z = x + y;
        System.out.println(z); //adding two numbers = result will be a number
        String s1 = "20"; 
        String s2 = "9";
        String s3 = s1 + s2;
        System.out.println(s3); //adding two strings = result will be a string concatenation
        String s4 = s1 + x;
        System.out.println(s4); //adding a number and string = result will be string concatenation
    }
}



